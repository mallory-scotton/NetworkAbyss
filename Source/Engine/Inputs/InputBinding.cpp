///////////////////////////////////////////////////////////////////////////////
// Dependencies
///////////////////////////////////////////////////////////////////////////////
#include <Engine/Inputs/InputBinding.hpp>

///////////////////////////////////////////////////////////////////////////////
// Namespace tkd
///////////////////////////////////////////////////////////////////////////////
namespace tkd
{

///////////////////////////////////////////////////////////////////////////////
InputBinding::InputBinding(void)
    : m_wasPressed(false)
{}

///////////////////////////////////////////////////////////////////////////////
Vec2f InputBinding::getAnalogValue(void) const
{
    return (Vec2f::zeroVector);
}

///////////////////////////////////////////////////////////////////////////////
void InputBinding::update(void)
{
    m_wasPressed = isPressed();
}

///////////////////////////////////////////////////////////////////////////////
bool InputBinding::isJustPressed(void) const
{
    return (isPressed() && !m_wasPressed);
}

///////////////////////////////////////////////////////////////////////////////
bool InputBinding::isJustReleased(void) const
{
    return (!isPressed() && m_wasPressed);
}

} // namespace tkd
