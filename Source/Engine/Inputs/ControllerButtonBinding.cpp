///////////////////////////////////////////////////////////////////////////////
// Dependencies
///////////////////////////////////////////////////////////////////////////////
#include <Engine/Inputs/ControllerButtonBinding.hpp>

///////////////////////////////////////////////////////////////////////////////
// Namespace tkd::Binds
///////////////////////////////////////////////////////////////////////////////
namespace tkd::Binds
{

///////////////////////////////////////////////////////////////////////////////
Button::Button(
    Uint32 controllerId,
    JoystickButton button
)
    : InputBinding()
    , m_controller(controllerId)
    , m_button(button)
{}

///////////////////////////////////////////////////////////////////////////////
bool Button::isPressed(void) const
{
    return (sf::Joystick::isButtonPressed(
        m_controller,
        static_cast<Uint32>(m_button)
    ));
}

///////////////////////////////////////////////////////////////////////////////
Uint32 Button::getControllerId(void) const
{
    return (m_controller);
}

} // namespace tkd::Binds
