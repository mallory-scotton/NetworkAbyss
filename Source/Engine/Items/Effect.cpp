///////////////////////////////////////////////////////////////////////////////
// Dependencies
///////////////////////////////////////////////////////////////////////////////
#include <Engine/Items/Effect.hpp>

///////////////////////////////////////////////////////////////////////////////
// Namespace tkd
///////////////////////////////////////////////////////////////////////////////
namespace tkd
{

///////////////////////////////////////////////////////////////////////////////
Effect::Effect(
    Effect::Type type,
    float value,
    Effect::StackingBehavior behavior
)
    : m_type(type)
    , m_behavior(behavior)
    , m_value(value)
{}

///////////////////////////////////////////////////////////////////////////////
Effect::Type Effect::getType(void) const
{
    return (m_type);
}

///////////////////////////////////////////////////////////////////////////////
Effect::StackingBehavior Effect::getStackingBehavior(void) const
{
    return (m_behavior);
}

///////////////////////////////////////////////////////////////////////////////
float Effect::getValue(void) const
{
    return (m_value);
}

} // namespace tkd
