///////////////////////////////////////////////////////////////////////////////
// Dependencies
///////////////////////////////////////////////////////////////////////////////
#include <Engine/Core/Renderer.hpp>

///////////////////////////////////////////////////////////////////////////////
// Namespace tkd
///////////////////////////////////////////////////////////////////////////////
namespace tkd
{

///////////////////////////////////////////////////////////////////////////////
Renderer::Renderer(const Vec2u& dimension, const String& title)
    : m_window(
        sf::VideoMode({dimension.x, dimension.y}),
        title,
        sf::Style::Close
    )
{}

///////////////////////////////////////////////////////////////////////////////
void Renderer::clear(void)
{
    m_window.clear();
}

///////////////////////////////////////////////////////////////////////////////
void Renderer::draw(const sf::Drawable& drawable)
{
    m_window.draw(drawable);
}

///////////////////////////////////////////////////////////////////////////////
void Renderer::display(void)
{
    m_window.display();
}

///////////////////////////////////////////////////////////////////////////////
void Renderer::setView(const sf::View& view)
{
    m_view = view;
    m_window.setView(m_view);
}

///////////////////////////////////////////////////////////////////////////////
sf::RenderWindow& Renderer::getWindow(void)
{
    return (m_window);
}

} // namespace tkd
